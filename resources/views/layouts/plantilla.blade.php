    var horas = document.getElementsByClassName('hora');

    for (var i = 0; i < fechas.length; i++) {
        var fechaHoraString = fechas[i].getAttribute('data-fecha');
        var fechaHoraParts = fechaHoraString.split(' ');

        var fecha = fechaHoraParts[0];
        var hora = fechaHoraParts[1];

        fechas[i].innerText = fecha;
        horas[i].innerText = hora;
    }
